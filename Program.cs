using System;
using System.Windows.Forms;

namespace GraphTeachingApp
{
    static class Program
    {
        /// <summary>
        /// النقطة الرئيسية لبدء تشغيل تطبيق تعليم الرسوم البيانية
        ///
        /// هذه الطريقة هي نقطة دخول البرنامج (Entry Point) وتتولى:
        /// 1. إعداد البيئة البصرية للتطبيق
        /// 2. تمكين التصميم الحديث للنوافذ (Visual Styles)
        /// 3. إنشاء وتشغيل النافذة الرئيسية للتطبيق
        ///
        /// سمة [STAThread] ضرورية للتطبيقات التي تستخدم:
        /// - Windows Forms
        /// - Clipboard (الحافظة)
        /// - بعض مكونات نظام Windows الأخرى
        ///
        /// خطوات بدء التشغيل:
        /// 1. تمكين التصميم البصري الحديث للحصول على مظهر أفضل
        /// 2. تعيين إعدادات التوافق للنصوص (مهم للغات RTL مثل العربية)
        /// 3. إنشاء instance من النافذة الرئيسية (MainForm)
        /// 4. بدء دورة رسائل Windows وتشغيل التطبيق
        /// </summary>
        /// <remarks>
        /// هذه الطريقة هي النقطة الوحيدة التي يبدأ منها تنفيذ البرنامج
        /// وتستمر في العمل حتى يغلق المستخدم التطبيق
        /// </remarks>
        [STAThread]
        static void Main()
        {
            // المرحلة الأولى: إعداد البيئة البصرية للتطبيق
            // تمكين التصميم الحديث للنوافذ يوفر مظهر أفضل وأكثر احترافية
            Application.EnableVisualStyles();
   
            // تعيين إعدادات التوافق للنصوص - مهم للغات RTL والنصوص المعقدة
            Application.SetCompatibleTextRenderingDefault(false);
   
            // المرحلة الثانية: إنشاء وتشغيل التطبيق
            // إنشاء instance من النافذة الرئيسية التي تحتوي على جميع وظائف التطبيق
            MainForm mainWindow = new MainForm();
   
            // بدء دورة رسائل Windows وتشغيل التطبيق
            // هذا الاستدعاء يبدأ التطبيق ويستمر حتى يتم إغلاقه بواسطة المستخدم
            Application.Run(mainWindow);
        }
    }
}